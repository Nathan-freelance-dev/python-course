#Day two: starting with python list

numbers = [1, 2, 3, 4, 5]
print(numbers[0 : 4])

numbers.append(6)
print(numbers)

numbers.insert(0, 6)
print(numbers)

numbers.remove(6)
print(numbers)

numbers.pop()
print(numbers)

numbers.index(5)
print(numbers)

numbers.sort()
print(numbers)

numbers.reverse()
print(numbers)

numbers.copy()
print(numbers)